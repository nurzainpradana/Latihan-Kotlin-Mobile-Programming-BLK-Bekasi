fun main(){
    /* String boleh menggunakan titik, spasi
     Variabel tidak boleh menggunakan spasi
     Nama Variabel itu Case Sensitive
     Bisa menggunakan Pascal Case / Camel Case
     PascalCase -> NaMaSaYa
     CamelCase -> namaSaya
     selanjutnya boleh menggunakan angka / underscore
     */

    // Deklarasi Variabel
    // Var adalah keyword untuk deklarasi
    // nama variabel adalah "namaSaya"
    // Value variable adalah "Nur Zain"
    // Value variable merupakan tipe data String
    // Tandanya dengan penggunaan simbol double quotes
    var namaSaya = "Nur Zain"

    println(namaSaya)

    println("Nama Saya $namaSaya")
}